        # Verify sensor arrays contain correct sensor names
        assert '{ name: "AI_Temp_S" }' in js
        assert '{ name: "DI_Start_S" }' in js
        assert '{ name: "DO_Heater_C" }' in js
        assert '{ name: "AO_Alarm_C" }' in js

        # Verify local variable
        assert 'let state = 0;' in js

        # Verify in_/out_ prefixes in body
        assert 'in_DI_Start_S' in js
        assert 'out_DO_Heater_C' in js

        # Verify if statement generated
        assert 'if (' in js


