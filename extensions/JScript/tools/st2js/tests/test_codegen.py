    def test_for_body_indented(self):
        from st2js.codegen import generate

        program = _make_program(
            locals_=[
                IRVariable(name="i", iec_type=IECType.INT, is_local=True),
                IRVariable(name="result", iec_type=IECType.INT, is_local=True),
            ],
            body=[
                IRForLoop(
                    var="i",
                    start=IRLiteral(value=1, iec_type=IECType.INT),
                    end=IRLiteral(value=10, iec_type=IECType.INT),
                    step=None,
                    body=[
                        IRAssignment(
                            target=IRVarRef(name="result"),
                            value=IRLiteral(value=0, iec_type=IECType.INT),
                        ),
                    ],
                ),
            ],
        )
        mapping = _make_mapping()
        js = generate(program, mapping)

        # Body assignment inside uniset_on_step should be indented deeper than the for line
        lines = js.split("\n")
        for_line = [l for l in lines if "for (let i" in l][0]
