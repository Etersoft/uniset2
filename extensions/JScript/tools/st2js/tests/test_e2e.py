"""End-to-end tests for the st2js converter.

Tests the full pipeline: ST file + YAML mapping -> JavaScript output.
Verifies that the generated JS matches the expected structure from the
Design Doc FR9 format:
  1. load("uniset2-iec61131.js")
  2. uniset_inputs array
  3. uniset_outputs array
  4. Local variable declarations
  5. function uniset_on_step() { ... }

Also tests CLI invocation via subprocess and error cases.
"""
from __future__ import annotations

import os
import subprocess
import sys
import tempfile

import pytest


FIXTURES_DIR = os.path.join(os.path.dirname(__file__), "fixtures")
MINIMAL_ST = os.path.join(FIXTURES_DIR, "minimal.st")
MINIMAL_MAPPING = os.path.join(FIXTURES_DIR, "minimal_mapping.yaml")
CONTROL_FLOW_ST = os.path.join(FIXTURES_DIR, "control_flow.st")
FB_CALLS_ST = os.path.join(FIXTURES_DIR, "fb_calls.st")
FB_CALLS_MAPPING = os.path.join(FIXTURES_DIR, "fb_calls_mapping.yaml")
FB_DECLARATION_ST = os.path.join(FIXTURES_DIR, "fb_declaration.st")
THERMOSTAT_ST = os.path.join(FIXTURES_DIR, "thermostat.st")
THERMOSTAT_MAPPING = os.path.join(FIXTURES_DIR, "thermostat_mapping.yaml")


class TestMinimalPipelineE2E:
    """Full pipeline: minimal.st + minimal_mapping.yaml -> JS output."""

    def _run_pipeline(self) -> str:
        """Run the full pipeline and return JS output string."""
        from st2js.codegen import generate
        from st2js.mapping import load_mapping
        from st2js.parser import parse_st
        from st2js.transformer import transform
        from st2js.type_system import check_types

        with open(MINIMAL_ST) as f:
            st_source = f.read()

        parse_result = parse_st(st_source, filename="minimal.st")
        ir_program = transform(parse_result)
        typed_program = check_types(ir_program)
        mapping = load_mapping(MINIMAL_MAPPING)

        return generate(typed_program, mapping)

    def test_contains_load_statement(self):
        js = self._run_pipeline()
        assert 'load("uniset2-iec61131.js");' in js

    def test_contains_uniset_inputs_array(self):
        js = self._run_pipeline()
        assert "uniset_inputs = [" in js

    def test_contains_uniset_outputs_array(self):
        js = self._run_pipeline()
        assert "uniset_outputs = [" in js

    def test_input_sensors_mapped(self):
        js = self._run_pipeline()
        assert '{ name: "AI_Temp_S" }' in js
        assert '{ name: "DI_Start_S" }' in js

    def test_output_sensors_mapped(self):
        js = self._run_pipeline()
        assert '{ name: "DO_Heater_C" }' in js
        assert '{ name: "AO_Alarm_C" }' in js

    def test_contains_uniset_on_step(self):
        js = self._run_pipeline()
        assert "function uniset_on_step()" in js

    def test_input_variable_substitution(self):
        """VAR_INPUT variables become in_<SensorName>."""
        js = self._run_pipeline()
        assert "in_AI_Temp_S" in js or "in_DI_Start_S" in js

    def test_output_variable_substitution(self):
        """VAR_OUTPUT variables become out_<SensorName>."""
        js = self._run_pipeline()
        assert "out_DO_Heater_C" in js or "out_AO_Alarm_C" in js

    def test_local_variable_declaration(self):
        js = self._run_pipeline()
        assert "let state = 0;" in js

    def test_if_statement_generated(self):
        js = self._run_pipeline()
        assert "if (" in js

    def test_js_structure_order(self):
        """Verify sections appear in correct order: load, inputs, outputs, vars, on_step."""
        js = self._run_pipeline()

        load_pos = js.index('load("uniset2-iec61131.js");')
        inputs_pos = js.index("uniset_inputs")
        outputs_pos = js.index("uniset_outputs")
        vars_pos = js.index("let state")
        on_step_pos = js.index("function uniset_on_step()")

        assert load_pos < inputs_pos < outputs_pos < vars_pos < on_step_pos

    def test_complete_output_snapshot(self):
        """Verify the complete JS output contains all expected elements."""
        js = self._run_pipeline()

        # All required structural elements
        required = [
            '// Generated by st2js from Main',
            'load("uniset2-iec61131.js");',
            "uniset_inputs = [",
            '{ name: "AI_Temp_S" }',
            '{ name: "DI_Start_S" }',
            "uniset_outputs = [",
            '{ name: "DO_Heater_C" }',
            '{ name: "AO_Alarm_C" }',
            "let state = 0;",
            "function uniset_on_step()",
            "in_DI_Start_S",
            "out_DO_Heater_C",
        ]

        for element in required:
            assert element in js, f"Missing expected element: {element!r}"


class TestCLIE2E:
    """End-to-end tests via CLI subprocess."""

