class TestErrorReportingE2E:
    """Tests that error types show correct file:line:col information."""

    def test_parse_error_shows_file_line_col(self):
        """ParseError from invalid ST should include file, line, and column."""
        from st2js.errors import ParseError
        from st2js.parser import parse_st

        invalid_st = "PROGRAM Test\nVAR\n    x : INT;\nEND_VAR\n  @@@ INVALID SYNTAX @@@\nEND_PROGRAM"
        with pytest.raises(ParseError) as exc_info:
            parse_st(invalid_st, filename="bad_program.st")

        err = exc_info.value
        assert err.file == "bad_program.st"
        # The error string should contain the filename
        error_str = str(err)
        assert "bad_program.st" in error_str

    def test_mapping_error_shows_message(self):
        """MappingError for invalid YAML includes descriptive message."""
        from st2js.errors import MappingError
        from st2js.mapping import load_mapping_from_string

        invalid_yaml = """
inputs:
  Temperature:
    no_sensor_field: AI_Temp_S
"""
        with pytest.raises(MappingError) as exc_info:
            load_mapping_from_string(invalid_yaml)

        err = exc_info.value
        error_str = str(err)
        assert "sensor" in error_str.lower()
        assert "Temperature" in error_str

    def test_unsupported_error_shows_file(self):
        """UnsupportedError includes file information."""
        from st2js.errors import UnsupportedError

        err = UnsupportedError(
            "POINTER not supported",
            file="complex.st",
            line=42,
            col=5,
        )
        error_str = str(err)
        assert error_str == "complex.st:42:5: error: POINTER not supported"

    def test_type_error_shows_message(self):
        """STTypeError includes descriptive type information."""
        from st2js.errors import STTypeError

        err = STTypeError(
            "cannot use STRING in arithmetic expression",
            file="types.st",
            line=10,
            col=3,
        )
        error_str = str(err)
        assert error_str == "types.st:10:3: error: cannot use STRING in arithmetic expression"

    def test_cli_parse_error_shows_on_stderr(self):
        """CLI should display parse error on stderr with non-zero exit."""
        with tempfile.NamedTemporaryFile(
            suffix=".st", mode="w", delete=False
        ) as f:
            f.write("PROGRAM Bad\nVAR\n  x : INT;\nEND_VAR\n  @@@ INVALID @@@\nEND_PROGRAM\n")
            bad_st = f.name

        try:
            result = subprocess.run(
                [
                    sys.executable, "-m", "st2js",
                    bad_st,
                    "-m", MINIMAL_MAPPING,
                ],
                capture_output=True,
                text=True,
            )
            assert result.returncode != 0
            assert "st2js:" in result.stderr
            assert "error" in result.stderr.lower()
        finally:
            os.unlink(bad_st)

    def test_cli_mapping_error_shows_on_stderr(self):
        """CLI should display mapping error on stderr for invalid YAML."""
        with tempfile.NamedTemporaryFile(
            suffix=".yaml", mode="w", delete=False
        ) as f:
            f.write("inputs:\n  Temp:\n    no_sensor: wrong\n")
            bad_yaml = f.name

        try:
            result = subprocess.run(
                [
                    sys.executable, "-m", "st2js",
                    MINIMAL_ST,
                    "-m", bad_yaml,
                ],
                capture_output=True,
                text=True,
            )
            assert result.returncode != 0
            assert "st2js:" in result.stderr
            assert "sensor" in result.stderr.lower()
        finally:
            os.unlink(bad_yaml)

    def test_all_error_types_are_sterror_subclasses(self):
        """Verify all error classes inherit from STError for consistent handling."""
        from st2js.errors import (
            STError,
            ParseError,
            STTypeError,
            MappingError,
            UnsupportedError,
        )

        for cls in [ParseError, STTypeError, MappingError, UnsupportedError]:
            assert issubclass(cls, STError), f"{cls.__name__} should inherit from STError"

    def test_all_error_types_format_with_location(self):
        """All error types should format file:line:col when location is provided."""
        from st2js.errors import ParseError, STTypeError, MappingError, UnsupportedError

        for cls in [ParseError, STTypeError, MappingError, UnsupportedError]:
            err = cls("test message", file="test.st", line=5, col=10)
            error_str = str(err)
            assert "test.st:5:10:" in error_str, f"{cls.__name__} should include file:line:col"
            assert "test message" in error_str, f"{cls.__name__} should include message"


class TestLoadDirectivesE2E:
    """End-to-end: load_head/load_on_start through full pipeline + qjsc."""

    def _minimal_st(self, tmp_path):
        import shutil
        st = tmp_path / "min.st"
        shutil.copy(MINIMAL_ST, st)
        return st

    def test_load_directives_emit_correctly_and_syntax_check(self, tmp_path):
        import shutil
        st = self._minimal_st(tmp_path)
        mapping = tmp_path / "map.yaml"
        mapping.write_text(
            "load_head:\n"
            "  - helpers/util.js\n"
            "load_on_start:\n"
            "  - project_sim.js\n"
        )
        out = tmp_path / "out.js"

        from st2js.cli import main
        rc = main([str(st), "-m", str(mapping), "-o", str(out)])
        assert rc == 0

        js = out.read_text()
        assert 'load("uniset2-iec61131.js");' in js
        assert 'load("helpers/util.js");' in js
        # load_on_start lands inside uniset_on_start body
        assert "function uniset_on_start() {\n    load(\"project_sim.js\");\n}" in js

        # qjsc syntax check (skip if qjsc not present on this machine)
        import shutil as _sh
        qjsc = _sh.which("qjsc")
        if qjsc:
            import subprocess
            result = subprocess.run(
                [qjsc, "-c", "-o", "/dev/null", str(out)],
                capture_output=True, text=True,
            )
            assert result.returncode == 0, f"qjsc failed: {result.stderr}"

    def test_cli_flags_override_yaml_absent(self, tmp_path):
        st = self._minimal_st(tmp_path)
        out = tmp_path / "out.js"

        from st2js.cli import main
        rc = main([
            str(st),
            "-o", str(out),
            "--load-head", "a.js",
            "--load-on-start", "b.js",
        ])
        assert rc == 0
        js = out.read_text()
        assert 'load("a.js");' in js
        assert 'load("b.js");' in js

    def test_load_head_skips_core_runtime(self, tmp_path):
        """If user redundantly lists uniset2-iec61131.js, it is not emitted twice."""
        st = self._minimal_st(tmp_path)
        mapping = tmp_path / "map.yaml"
        mapping.write_text(
            "load_head:\n"
            "  - uniset2-iec61131.js\n"
        )
        out = tmp_path / "out.js"

        from st2js.cli import main
        rc = main([str(st), "-m", str(mapping), "-o", str(out)])
        assert rc == 0
        js = out.read_text()
        assert js.count('load("uniset2-iec61131.js");') == 1
